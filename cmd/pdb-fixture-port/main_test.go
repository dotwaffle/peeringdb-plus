// TestParenDelta exercises the paren-depth scanner's string-awareness
// directly. Strings in Python fixture bodies may legitimately contain
// parens; the scanner must ignore them.
func TestParenDelta(t *testing.T) {
	t.Parallel()
	cases := []struct {
		name string
		in   string
		want int
	}{
		{name: "balanced", in: "foo(bar)", want: 0},
		{name: "open", in: "foo(", want: 1},
		{name: "close", in: ")", want: -1},
		{name: "string_quote_hides_paren", in: `"abc(def"`, want: 0},
		{name: "single_quote_hides_paren", in: `'abc(def'`, want: 0},
		{name: "nested_call", in: `foo(bar(baz))`, want: 0},
		{name: "escaped_quote_inside_string", in: `"a\"b("`, want: 0},
	}
	for _, tc := range cases {
		t.Run(tc.name, func(t *testing.T) {
			t.Parallel()
			if got := parenDelta(tc.in); got != tc.want {
				t.Errorf("parenDelta(%q) = %d, want %d", tc.in, got, tc.want)
			}
		})
	}
}

// TestExtractFields exercises the kwargs parser against the common
// shapes seen in upstream pdb_api_test.py.
func TestExtractFields(t *testing.T) {
	t.Parallel()
	block := "Network.objects.create(\n" +
		"    status=\"ok\",\n" +
		"    name=\"OrderNet-A\",\n" +
		"    asn=65001,\n" +
		"    # comment line — ignored\n" +
		"    **self.make_data_net(),\n" +
		")\n"
	got := extractFields(block)
	want := map[string]string{
		"status": "\"ok\"",
		"name":   "\"OrderNet-A\"",
		"asn":    "65001",
	}
	for k, v := range want {
		if got[k] != v {
			t.Errorf("field %q = %q, want %q", k, got[k], v)
		}
	}
	// `**self.make_data_net()` must be skipped.
	if _, ok := got["**self"]; ok {
		t.Errorf("kwargs-splat should be skipped; fields=%v", got)
	}
}

// TestOrderingTimestampsMonotonic locks the synthesised-timestamp
// contract the parity tests consume: later-line rows MUST carry
// later `created` timestamps (orderingCreatedAt is strictly increasing
// in line). Guards against future refactors that might accidentally
// introduce clock-skew or non-deterministic seeding.
func TestOrderingTimestampsMonotonic(t *testing.T) {
	t.Parallel()
	earlier := orderingCreatedAt(100)
	later := orderingCreatedAt(200)
	if !earlier.Before(later) {
		t.Errorf("created(100)=%v should be before created(200)=%v", earlier, later)
	}
	// `updated` offset from `created` by +24h per line.
	updated := orderingUpdatedAt(100)
	if !earlier.Before(updated) {
		t.Errorf("created(100)=%v should be before updated(100)=%v", earlier, updated)
	}
}

// TestSynthIDStability locks the ID-derivation contract: same inputs
// produce same ID on every call. A parity consumer should never see
// an ID shift between tool runs with identical upstream bytes.
func TestSynthIDStability(t *testing.T) {
	t.Parallel()
	fields := map[string]string{"name": "OrderNet-A", "asn": "65001"}
	a := synthID("net", 100, fields)
	b := synthID("net", 100, fields)
	if a != b {
		t.Errorf("synthID not stable: %d vs %d", a, b)
	}
	// Different line = different ID (high-probability collision check).
	c := synthID("net", 101, fields)
	if a == c {
		t.Errorf("synthID collided across lines 100/101 for %v: both %d", fields, a)
	}
}

