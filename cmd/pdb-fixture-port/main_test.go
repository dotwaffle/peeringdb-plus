// TestParenDelta exercises the paren-depth scanner's string-awareness
// directly. Strings in Python fixture bodies may legitimately contain
// parens; the scanner must ignore them.
func TestParenDelta(t *testing.T) {
	t.Parallel()
	cases := []struct {
		name string
		in   string
		want int
	}{
		{name: "balanced", in: "foo(bar)", want: 0},
		{name: "open", in: "foo(", want: 1},
		{name: "close", in: ")", want: -1},
		{name: "string_quote_hides_paren", in: `"abc(def"`, want: 0},
		{name: "single_quote_hides_paren", in: `'abc(def'`, want: 0},
		{name: "nested_call", in: `foo(bar(baz))`, want: 0},
		{name: "escaped_quote_inside_string", in: `"a\"b("`, want: 0},
	}
	for _, tc := range cases {
		t.Run(tc.name, func(t *testing.T) {
			t.Parallel()
			if got := parenDelta(tc.in); got != tc.want {
				t.Errorf("parenDelta(%q) = %d, want %d", tc.in, got, tc.want)
			}
		})
	}
}

