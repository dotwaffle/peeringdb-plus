const testDataMinPy = "testdata/pdb_api_test_min.py"

// TestFixturePort_HelpExitsZero asserts `--help` is a no-op success
// (flag.ContinueOnError + ErrHelp path). Guards against regressions
// where flag.Parse returns a non-help error on `--help`.
func TestFixturePort_HelpExitsZero(t *testing.T) {
	t.Parallel()
	var stdout, stderr bytes.Buffer
	code := run([]string{"--help"}, &stdout, &stderr)
	if code != exitOK {
		t.Fatalf("run --help = %d, want %d (stderr=%q)", code, exitOK, stderr.String())
	}
	combined := stderr.String() + stdout.String()
	if !strings.Contains(combined, "pdb-fixture-port") {
		t.Errorf("--help output missing tool name; got %q", combined)
	}
	if !strings.Contains(combined, "--check") {
		t.Errorf("--help output missing --check flag; got %q", combined)
	}
}

// TestFixturePort_EmitsSHAHeader locks the header contract the
// runtime consumers rely on (DO NOT EDIT marker, upstream git SHA,
// sha256 hash). Matches must_haves truth #3 in 72-01-PLAN.md.
func TestFixturePort_EmitsSHAHeader(t *testing.T) {
	t.Parallel()
	tmp := filepath.Join(t.TempDir(), "fixtures.go")
	args := []string{
		"--upstream-file", testDataMinPy,
		"--out", tmp,
		"--category", "ordering",
		"--date", "2026-04-19",
	}
	var out, errb bytes.Buffer
	code := run(args, &out, &errb)
	if code != exitOK {
		t.Fatalf("run() = %d, want %d; stderr=%q", code, exitOK, errb.String())
	}
	body, err := os.ReadFile(tmp) // #nosec G304 — test-controlled tempdir path.
	if err != nil {
		t.Fatalf("read output: %v", err)
	}
	// Assemble the expected sha256 from the input file so this test
	// continues to pass after testdata updates.
	in, err := os.ReadFile(testDataMinPy) // #nosec G304 — static testdata path.
	if err != nil {
		t.Fatalf("read testdata: %v", err)
	}
	want := sha256.Sum256(in)
	wantHex := hex.EncodeToString(want[:])

	mustContain := []string{
		"Code generated by cmd/pdb-fixture-port",
		"DO NOT EDIT",
		"Upstream:     peeringdb/peeringdb@",
		"UpstreamHash: sha256:" + wantHex,
		"Source:       " + upstreamPath,
		"Category:     ordering",
		"Ported:       2026-04-19",
		"package parity",
	}
	for _, needle := range mustContain {
		if !strings.Contains(string(body), needle) {
			t.Errorf("output missing %q", needle)
		}
	}
}

// TestFixturePort_DeterministicRender runs the tool twice against the
// same inputs and asserts byte-identical output. Guards against
// accidental non-determinism (map iteration order, time.Now() calls
// in the body, unsorted slices). Matches must_haves truth #5.
func TestFixturePort_DeterministicRender(t *testing.T) {
	t.Parallel()
	tmpDir := t.TempDir()
	pathA := filepath.Join(tmpDir, "a.go")
	pathB := filepath.Join(tmpDir, "b.go")
	baseArgs := []string{
		"--upstream-file", testDataMinPy,
		"--category", "ordering",
		"--date", "2026-04-19",
	}

	var outA, errA bytes.Buffer
	argsA := append([]string{}, baseArgs...)
	argsA = append(argsA, "--out", pathA)
	if code := run(argsA, &outA, &errA); code != exitOK {
		t.Fatalf("run #1 = %d; stderr=%q", code, errA.String())
	}
	var outB, errB bytes.Buffer
	argsB := append([]string{}, baseArgs...)
	argsB = append(argsB, "--out", pathB)
	if code := run(argsB, &outB, &errB); code != exitOK {
		t.Fatalf("run #2 = %d; stderr=%q", code, errB.String())
	}
	a, err := os.ReadFile(pathA) // #nosec G304 — tempdir.
	if err != nil {
		t.Fatalf("read A: %v", err)
	}
	b, err := os.ReadFile(pathB) // #nosec G304 — tempdir.
	if err != nil {
		t.Fatalf("read B: %v", err)
	}
	hashA := sha256.Sum256(a)
	hashB := sha256.Sum256(b)
	if hashA != hashB {
		t.Fatalf("two runs produced different output:\n A sha256=%x (%d bytes)\n B sha256=%x (%d bytes)", hashA, len(a), hashB, len(b))
	}
}

